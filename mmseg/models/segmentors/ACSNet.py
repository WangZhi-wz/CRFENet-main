def conv2d(in_channel, out_channel, kernel_size):
    layers = [
        nn.Conv2d(in_channel, out_channel, kernel_size, padding=kernel_size // 2, bias=False),
        nn.BatchNorm2d(out_channel),
        nn.ReLU(),
    ]

    return nn.Sequential(*layers)


def conv1d(in_channel, out_channel):
    layers = [
        nn.Conv1d(in_channel, out_channel, 1, bias=False),
        nn.BatchNorm1d(out_channel),
        nn.ReLU(),
    ]

    return nn.Sequential(*layers)



""" Local Context Attention Module"""

class LCA(nn.Module):
    def __init__(self):
        super(LCA, self).__init__()

        self.out = nn.Sequential(
            conv2d(64, 32, 1),
            nn.Conv2d(32, 1, 1)
        )

    def forward(self, x, pred):
        residual = x
        score = torch.sigmoid(pred)
        dist = torch.abs(score - 0.5)
        att = 1 - (dist / 0.5)
        att_x = x * att
        out = att_x + residual
        return out

